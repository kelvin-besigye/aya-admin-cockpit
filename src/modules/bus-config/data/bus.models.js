import { BUS_CLASSES } from './bus.constants';

/**
 * BUS CONFIGURATION MODELS (The Blueprint Factory)
 * ------------------------------------------------------------------
 * Defines the DNA for a Service Blueprint (e.g., "Gateway VIP Layout").
 *
 * ARCHITECTURE NOTES (Chassis Grammar v2):
 * 1. HYBRID ALIGNMENT: Uses CamelCase for React State keys (partnerId)
 *    but aligns inner objects (layout, images) with the specific
 *    component requirements.
 * 2. PURE SERVICE: No physical hardware details (Plates/VINs).
 * 3. DEFAULT STATE: Pre-configured for the Ugandan market.
 * 4. SCHEMA v2: layout_config now carries driver_position, entrance_side,
 *    entrance_row, bench_position, conductor_count, conductor_side,
 *    has_invalid_seat, invalid_seat_side.
 */

export const createDefaultBusConfig = () => {
  // Default to the most common bus type in Uganda (Standard/Ordinary)
  const defaultClass = BUS_CLASSES.find(c => c.id === 'STANDARD');

  return {
    id: '', // Generated by Database

    // === STEP 1: IDENTITY ===
    partnerId: '',       // The Operator (e.g. Gateway)
    busClass: '',        // The Service Level (e.g. VIP)

    // === STEP 2: CHASSIS ENGINE (Schema v2) ===
    layout: {
      total_rows: 11,
      cols_left: 2,
      cols_right: 3,
      has_rear_bench: true,

      // ── NEW in v2 ──
      driver_position: 'RIGHT',     // 'LEFT' | 'RIGHT'
      entrance_side:   'NONE',      // 'NONE' | 'LEFT' | 'RIGHT'
      entrance_row:    1,           // 1-indexed (only used if entrance_side !== 'NONE')
      bench_position:  'MIDDLE',    // 'MIDDLE' | 'RIGHT' (legacy)
      conductor_count: 0,           // 0 | 1 | 2
      conductor_side:  'LEFT',      // 'LEFT' | 'RIGHT'
      has_invalid_seat: false,      // toggleable 1X (wheelchair / reserved)
      invalid_seat_side: 'LEFT',    // 'LEFT' | 'RIGHT'

      total_seats: 0,               // Auto-calculated by calculateSeatCount

      // ── Legacy override for special front rows (door-zone, etc.) ──
      front_rows: [],
    },

    // === STEP 3: VISUAL PROFILE ===
    profile_image: null,
    gallery_images: [],

    // === STEP 4: AMENITIES ===
    amenities: [],

    // META
    status: 'DRAFT',
    schema_version: 2,
    createdAt: new Date().toISOString()
  };
};

/**
 * VALIDATION ENGINE
 * Used by the BusWizardContainer to unlock "Next" buttons.
 */
export const isStepValid = (stepId, data) => {
  if (!data) return false;

  switch (stepId) {
    case 1: // IDENTITY
      return !!(data.partnerId && data.busClass);

    case 2: { // CHASSIS (v2 — includes orientation)
      const layout = data.layout;
      if (!layout || layout.total_rows < 5) return false;

      // Driver + entrance can't be on the same side of the same row
      const isSameRow = layout.entrance_side !== 'NONE' && layout.entrance_row === 1;
      const sameSide  = isSameRow && layout.driver_position === layout.entrance_side;
      if (sameSide) return false;

      // Entrance row must be within total_rows
      if (layout.entrance_row < 1 || layout.entrance_row > layout.total_rows) return false;

      return true;
    }

    case 3: { // VISUALS
      const hasProfile = !!data.profile_image;
      const galleryCount = data.gallery_images?.length || 0;
      return hasProfile && galleryCount >= 3;
    }

    case 4: // AMENITIES
      return true;

    default:
      return false;
  }
};