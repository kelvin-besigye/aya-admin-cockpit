import { BUS_CLASSES } from './bus.constants';

/**
 * BUS CONFIGURATION MODELS (The Blueprint Factory)
 * ------------------------------------------------------------------
 * Defines the DNA for a Service Blueprint (e.g., "Gateway VIP Layout").
 * * * ARCHITECTURE NOTES (The "World Class" Standard):
 * 1. HYBRID ALIGNMENT: Uses CamelCase for React State keys (partnerId) 
 * but aligns inner objects (layout, images) with the specific 
 * component requirements you uploaded.
 * 2. PURE SERVICE: No physical hardware details (Plates/VINs).
 * 3. DEFAULT STATE: Pre-configured for the Ugandan market (Ordinary 2x3).
 */

export const createDefaultBusConfig = () => {
  
  // Default to the most common bus type in Uganda (Standard/Ordinary)
  // This ensures the form is never "blank" or "undefined"
  const defaultClass = BUS_CLASSES.find(c => c.id === 'STANDARD');

  return {
    id: '', // Generated by Database
    
    // === STEP 1: IDENTITY (CamelCase to match Step1_Class.jsx) ===
    partnerId: '',       // The Operator (e.g. Gateway)
    busClass: '',        // The Service Level (e.g. VIP)
    
    // === STEP 2: CHASSIS ENGINE (Snake_case to match Step2_Layout.jsx) ===
    layout: {
      total_rows: 11,     // Standard length (Min 5)
      cols_left: 2,       // Standard 2 seats (Door side)
      cols_right: 3,      // Standard 3 seats (Driver side)
      has_rear_bench: true, // The "M" seat (5 at back)
      driver_position: 'RIGHT', // Fixed for Uganda
      total_seats: 0      // Auto-calculated by the Canvas
    },

    // === STEP 3: VISUAL PROFILE (Snake_case to match Step3_Visuals.jsx) ===
    profile_image: null,  // Main cover photo (Blob or URL)
    gallery_images: [],   // Array of detail photos (Blobs or URLs)

    // === STEP 4: AMENITIES (CamelCase Array) ===
    amenities: [],       // Array of IDs ['wifi', 'ac']

    // META
    status: 'DRAFT',
    createdAt: new Date().toISOString()
  };
};

/**
 * VALIDATION ENGINE
 * Used by the BusWizardContainer to unlock "Next" buttons.
 * Strictly adheres to the field names defined above.
 */
export const isStepValid = (stepId, data) => {
  if (!data) return false;

  switch (stepId) {
    case 1: // IDENTITY
      // Must have an Operator and a Class selected
      return !!(data.partnerId && data.busClass);
      
    case 2: // CHASSIS
      // Must have valid rows (Min 5 per constraints)
      return data.layout && data.layout.total_rows >= 5;
      
    case 3: // VISUALS
      // Requirement: 1 Profile + Min 3 Gallery = 4 Total
      const hasProfile = !!data.profile_image;
      const galleryCount = data.gallery_images?.length || 0;
      return hasProfile && galleryCount >= 3;
      
    case 4: // AMENITIES
      // Optional, but always valid to allow skipping
      return true; 
      
    default:
      return false;
  }
};